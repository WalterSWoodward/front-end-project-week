app.use(bodyParser.json());

app.use(cors());

app.get('/api/notes', (req, res) => {
  res.send(notes);
});

app.get('/api/notes/:id', (req, res) => {
  const friend = notes.find(f => f.id == req.params.id);

  if (friend) {
    res.status(200).json(friend);
  } else {
    res.status(404).send({
      msg: 'Friend not found'
    });
  }
});

app.post('/api/notes', (req, res) => {
  const friend = {
    id: getNextId(),
    ...req.body
  };

  notes = [...notes, friend];

  res.send(notes);
});

app.put('/api/notes/:id', (req, res) => {
  const {
    id
  } = req.params;

  const friendIndex = notes.findIndex(f => f.id == id);

  if (friendIndex > -1) {
    const friend = { ...notes[friendIndex],
      ...req.body
    };

    notes = [
      ...notes.slice(0, friendIndex),
      friend,
      ...notes.slice(friendIndex + 1),
    ];
    res.send(notes);
  } else {
    res.status(404).send({
      msg: 'Friend not found'
    });
  }
});

app.delete('/api/notes/:id', (req, res) => {
  const {
    id
  } = req.params;

  notes = notes.filter(f => f.id !== Number(id));

  res.send(notes);
});

function getNextId() {
  return nextId++;
}

app.listen(port, () => {
  console.log(`server listening on port ${port}`);
});